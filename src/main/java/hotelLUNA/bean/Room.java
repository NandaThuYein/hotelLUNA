package hotelLUNA.bean;

public class Room {
	
	private String rid;
	private String rcapacity;
	private String rprice;
	private String rtype;
	private String avaliable;
	private String rtypename;
	/**
	 * @return the rid
	 */
	public String getRid() {
		return rid;
	}
	/**
	 * @param rid the rid to set
	 */
	public void setRid(String rid) {
		this.rid = rid;
	}
	/**
	 * @return the rcapacity
	 */
	public String getRcapacity() {
		return rcapacity;
	}
	/**
	 * @param rcapacity the rcapacity to set
	 */
	public void setRcapacity(String rcapacity) {
		this.rcapacity = rcapacity;
	}
	/**
	 * @return the rprice
	 */
	public String getRprice() {
		return rprice;
	}
	/**
	 * @param rprice the rprice to set
	 */
	public void setRprice(String rprice) {
		this.rprice = rprice;
	}
	/**
	 * @return the rtype
	 */
	public String getRtype() {
		return rtype;
	}
	/**
	 * @param rtype the rtype to set
	 */
	public void setRtype(String rtype) {
		this.rtype = rtype;
	}
	/**
	 * @return the avaliable
	 */
	public String getAvaliable() {
		return avaliable;
	}
	/**
	 * @param avaliable the avaliable to set
	 */
	public void setAvaliable(String avaliable) {
		this.avaliable = avaliable;
	}
	public String getRtypename() {
		return rtypename;
	}
	public void setRtypename(String rtypename) {
		this.rtypename = rtypename;
	}
	
	

}
